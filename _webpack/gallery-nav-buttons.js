const backwardGalleryButton = document.getElementById("backward-gallery");
const forwardGalleryButton = document.getElementById("forward-gallery");
const backwardPictureButton = document.getElementById("backward-picture");
const forwardPictureButton = document.getElementById("forward-picture");
const galleryTitle = document.getElementById('gallery-title');
const picture = document.getElementById('picture');
const pictureDetails = document.getElementById('picture-description');

const galleries = Object.keys(contentGalleries);
let currentGallery = {};
let picturesInCurrentGallery = [];
const setDefaults = () => {
    const defaultGallery = galleries[0];
    picturesInCurrentGallery = Object.keys(contentGalleries[defaultGallery]).slice(1);
    const defaultPicture = picturesInCurrentGallery[0];
    currentGallery = contentGalleries[defaultGallery];
    galleryTitle.innerHTML = currentGallery.title;
    picture.src = contentGalleries[defaultGallery][defaultPicture].photo;
    pictureDetails.innerHTML = contentGalleries[defaultGallery][defaultPicture].title + ": " + contentGalleries[defaultGallery][defaultPicture].description;
};

setDefaults();
backwardGalleryButton.addEventListener("click", function(){selectGallery('backward')});
forwardGalleryButton.addEventListener("click", function() {selectGallery('forward')});
let currentGalleryIndex = 0;

const setCurrents = (selectedGallery) => {
    currentGallery = selectedGallery;
    picturesInCurrentGallery = Object.keys(currentGallery).slice(1);
    const currentPicture = picturesInCurrentGallery[0];
    galleryTitle.innerHTML = currentGallery.title;
    picture.src = currentGallery[currentPicture].photo;
    pictureDetails.innerHTML = currentGallery[currentPicture].title + ": " + currentGallery[currentPicture].description;
}
function selectGallery(direction) {
    if (direction === 'backward'){
        if (currentGalleryIndex <= 0) {
            currentGalleryIndex = galleries.length - 1;
        } else {
            currentGalleryIndex -= 1;
        }
    } else {
        if (currentGalleryIndex >= galleries.length-1) {
            currentGalleryIndex = 0;
        } else {
            currentGalleryIndex += 1;
        }
    }

    const currentGallery = contentGalleries[galleries[currentGalleryIndex]];
    setCurrents(currentGallery);
}

